struct point2D {
	float x, y;
};

/* A piece is a set of consecutive points that should have edges drawn to connect them all into a figure.
Many letters have only one piece, but some require multiple. For example, the letter O has an inner ring and outer ring of connected points, so it has two pieces.
To same time writing out all of the connected points in a letter, we just store the starting and ending point in each piece and make the edges later.*/
struct piece {
	int first, last;
};

struct letter {
	char name;
	std::vector<point2D> points;
	std::vector<piece> pieces;
	letter(char name, std::vector<point2D> points, std::vector<piece> pieces) : name(name), points(points), pieces(pieces) {}
};


